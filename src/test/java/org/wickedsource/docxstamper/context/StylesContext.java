package org.wickedsource.docxstamper.context;

public class StylesContext {

    private String styleBold;
    private String styleItalic;
    private String styleNormal;
    private String styleSupper;

    public String getStyleBold() {
        return styleBold;
    }

    public void setStyleBold(final String styleBold) {
        this.styleBold = styleBold;
    }

    public String getStyleItalic() {
        return styleItalic;
    }

    public void setStyleItalic(final String styleItalic) {
        this.styleItalic = styleItalic;
    }

    public String getStyleNormal() {
        return styleNormal;
    }

    public void setStyleNormal(final String styleNormal) {
        this.styleNormal = styleNormal;
    }

    public String getStyleSupper() {
        return styleSupper;
    }

    public void setStyleSupper(final String styleSupper) {
        this.styleSupper = styleSupper;
    }
}

